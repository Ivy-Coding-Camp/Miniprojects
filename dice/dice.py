from random import randint #we import the randint function from the library random
print("The result was " + str(randint(1,6)))
#We print out the result, and we combine a string and an int. To combine these, we cast (transform) the int to a string using the 'str' function
#randint returns an integer including 1 and 6, and between them. Remember, ints don't have decimal places.